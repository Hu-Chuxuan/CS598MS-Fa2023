SELECT DISTINCT TITLE_PRINCIPALS.CHARACTERS FROM TITLE_CREW, TITLE_BASELINE, TITLE_PRINCIPALS WHERE
(TITLE_CREW.DIRECTORS = "Guy Pearce" AND TITLE_BASELINE.GENRES LIKE "%drama%" OR TITLE_BASELINE.GENRES LIKE "%comedy%") AND
(TITLE_CREW.WRITER IN ("John Collee", "Matthew Saville")) AND
(TITLE_BASELINE.ISADULT IS NOT NULL AND TITLE_BASELINE.ISADULT <> "true") AND
(TITLE_BASELINE.STARTYEAR BETWEEN "2006" AND "2010") AND
(TITLE_BASELINE.ENDYEAR < TIMESTAMPADD("MONTH", DATEPART("DAY", GETDATE())+9, CAST((TITLE_BASELINE.STARTYEAR + TITLE_BASELINE.ENDYEAR)/2 AS FLOAT)) )AND
(TITLE_BASELINE.TIMEMINUTES >= 60)