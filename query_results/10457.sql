SELECT * FROM
( SELECT DISTINCT TITLE_BASICS.*,
    COUNT(*) AS RATINGSCOUNT
FROM   TITLE_BASES INNER JOIN
     TITLE_RATINGS ON ((TITLE_BASES.titleId = TITLE_RATINGS.titleId)) WHERE
     ISADULT=1 AND STARTYEAR>1960 GROUP BY TITLE_BASICS.titleID )
INNER JOIN
( SELECT CONCAT('"', TITLE_PRINCIPALS.job,'", "', TITLE_CREW.director,"'") as CREWDONE
FROM   TITLE_CREW LEFT OUTER JOIN TITLE_PRINCIPALS USING ('titleId') WHERE director IN ("Directed By","Screenplay Written by"))
ON (TITLE_BASES.primaryName LIKE '%"%') ORDER BY rating DESC LIMIT 2